package c5_operator;

public class Operator4 {

	public static void main(String[] args) {
		
		int n1=10;
		int n2=5;
		boolean result;
		
		result = n1>n2;
		System.out.println(result);

		result = n1<n2;
		System.out.println(result);
		
		result = n1>=n2;
		System.out.println(result);
		
		result = n1<=n2;
		System.out.println(result);
		
		result = n1 == n2; //같으냐
		System.out.println(result);
		
		result = n1 !=n2; //다르냐
		System.out.println(result);
		
		
		
	}

}
